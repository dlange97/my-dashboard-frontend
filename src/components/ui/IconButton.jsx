export default function IconButton({
  icon,
  variant = "default",
  className = "",
  children,
  ...props
}) {
  const classes = [
    "icon-btn",
    variant !== "default" ? `icon-btn--${variant}` : "",
    className,
  ]
    .filter(Boolean)
    .join(" ");

  return (
    <button type="button" className={classes} {...props}>
      {ICONS[icon] || children}
    </button>
  );
}

